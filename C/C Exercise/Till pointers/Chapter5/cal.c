#include "functions.h"

int addition (int a, int b)

{
    int sum;

    sum = a + b;

    return sum;
}

int subtraction (int a, int b)

{
    int sub;

    sub = a - b;

    return sub;
}

int multiplication (int a, int b)

{
    int mul;

    mul = a * b;

    return mul;
}

int division (int a, int b)

{
    int div;

    div = a / b;

    return div;
}
